
public class Switchtest {
	public static void main(String args[]) {
		int a=15;
		switch(a) {
		case 10 : System.out.println("10");
				  break;
		case 15 : System.out.println("15");
				  break;
		case 20 : System.out.println("20");
				  break;
	    default : System.out.println("NOT 10,15 or 20");
	    		  break;
	    		  
		}
		
	}
}
